package Entornos2;

public class Calculadora {
	public Calculadora(){
		super();
	}
	
	public static  int Sumar(int num1, int num2) {
		int suma = num1 + num2 ;
		
		return suma;
		
	}
	
	public static int Restar(int num1, int num2) {
		int resta = num1 - num2 ;
		return resta;
		
	}
	
	
	
	
	
	
	

}

